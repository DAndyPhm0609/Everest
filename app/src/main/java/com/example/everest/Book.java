package com.example.everest;

public class Book {
    String name;
    String author;
    String price;
    String rating;
    String category;
    String state;

    public Book(String name, String author, String price, String rating, String category, String state){
        this.name = name;
        this.author = author;
        this.price = price;
        this.rating = rating;
        this.category = category;
        this.state = state;
    }
}
